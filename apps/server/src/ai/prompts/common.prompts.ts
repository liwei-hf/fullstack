/**
 * 通用回答风格约束
 *
 * 这层专门放多条 AI 链路都能复用的回答规范，
 * 避免“先给结论、使用中文、不要编造”这类规则散落在不同 prompt 文件里。
 */
export function buildCommonChineseAnswerRules() {
  return [
    '请使用中文回答。',
    '先给结论，再补充关键依据或关键观察。',
    '不要编造结果，只能依据给定信息回答。',
    '输出内容适合流式展示和手机端阅读。',
  ].join('\n');
}

/**
 * 当结果为空或上下文不足时的统一兜底表达。
 *
 * 不同链路可以按场景补充自己的限制，但缺省话术尽量保持一致，
 * 这样前后端展示和用户体验会更统一。
 */
export function buildCommonInsufficientInfoRules() {
  return [
    '如果没有数据，要明确说“当前没有查到符合条件的数据”。',
    '如果上下文不能支持完整回答，要明确说明“当前信息不足，无法确认”。',
  ].join('\n');
}
